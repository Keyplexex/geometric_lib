def area(a, h):
    '''Takes a, h (int, float) - side and height of a triangle, returns its area - a * h / 2 (int, float)''' 
    return a * h / 2 

def perimeter(a, b, c):
    '''Takes a, b, and c (int, float) - sides of a triangle, returns its perimeter - a + b + c (int, float)''' 
    return a + b + c
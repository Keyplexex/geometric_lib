def area(b, h):
    '''Takes b (int, float) - base and h (int, float) - height of the parallelogram, returns its area - b * h (int, float)'''
    return b * h

def perimeter(a, b):
    '''Takes a and b (int, float) - sides of the parallelogram, returns its perimeter - 2 * (a + b) (int, float)'''
    return 2 * (a + b)